#include <iostream> // 包含头文件

using namespace std; // 使用标准命名空间

int main() { // 主函数是程序的入口
    // 输出字符串 "Hello, World!"
    cout << "Hello, World!" << endl;

    // 将 3 个数字进行拼接
    cout << 1 << 2 << 3 << endl;
    // 输出两个整数的和
    cout << 1 + 2 << endl;
    // 输出字符串
    cout << "1 + 2" << endl;

    // 加减乘除
    cout << 1 * 2 + 3 * 4 + 5 * 6 + 7 * 8 + 9 * 0;

    // 输出金字塔
    cout << "   *" << endl;
    cout << "  ***" << endl;
    cout << " *****" << endl;
    cout << "*******" << endl;

    // 输出自己的名字
    cout << "Reacher";

    return 0; // 函数的返回值为 0
}